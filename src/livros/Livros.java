package livros;

public class Livros {
    private String titulo;
    private String autor;
    private int ano;
    private String disponibilidade;

    public Livros (String titulo, String autor, int ano, String disponibilidade) {
        this.titulo = titulo;
        this.autor = autor;
        this.ano = ano;
        this.disponibilidade = disponibilidade;
    }
}
